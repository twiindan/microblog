    dancing = db.Column(db.Boolean, default=False)
    travel = db.Column(db.Boolean, default=False)
    cinema = db.Column(db.Boolean, default=False)
    music = db.Column(db.Boolean, default=False)
    user_id = db.Column(db.Integer, db.ForeignKey('user.id'))


